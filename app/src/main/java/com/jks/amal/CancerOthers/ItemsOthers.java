package com.jks.amal.CancerOthers;

public class ItemsOthers {

    String Title;
    String Description;
    int CircleImageView;

    public ItemsOthers(String title, String description, int circleImageView) {
        Title = title;
        Description = description;
        CircleImageView = circleImageView;
    }

    public String getTitle() {
        return Title;
    }

    public String getDescription() {
        return Description;
    }

    public int getCircleImageView() {
        return CircleImageView;
    }

}
